//SWITCH

let num = 3;

switch(num) {
    case 0:
        console.log("Numero oli: ",num);
        break;
    case 3:
        console.log("Numero oli: ",num);
        break;
    default:                               //tänne mennään jos mikään case ei toteudu "else"
        console.log("Numero oli joka muu");
}